def read_data(DataFile):
    with open(DataFile,"r") as InFile:
        Data = pd.DataFrame.from_csv(InFile)
        Data = Data.drop("title", axis=1)
        Data = Data.drop("genre", axis=1)
        Data = Data.drop("subgenre", axis=1)
        Data = Data.drop("author", axis=1)
        Data = Data.drop("author-gender", axis=1)
        Data = Data.drop("words", axis=1)
        Data = Data.drop("sents", axis=1)
        Data = Data.drop("median", axis=1)
        Data = Data.drop("stdev", axis=1)
        Data = Data.drop("pub-year", axis=1)
        #print(Data.head())
        return Data

def make_datedata(Data): 
    Data = Data.groupby("series")
    #print(Data.head())
    Dicts = Data.get_group("dicts")
    DictsYears = []
    DictsMeans = []
